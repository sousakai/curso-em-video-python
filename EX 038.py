primeiroNum = int(input('Digite o primeiro número:\n'))
segundoNum = int(input('Digite o segundo número: \n'))
if(primeiroNum > segundoNum):
    print(f'O primeiro número ({primeiroNum}) é maior que o segundo ({segundoNum}).')
elif(segundoNum > primeiroNum):
    print(f'O segundo número ({segundoNum}) é maior do que o primeiro ({primeiroNum})')
else:
    print(f'Os dois números ({primeiroNum}) e ({segundoNum}) são iguais.)')